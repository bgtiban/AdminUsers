package admin.users.exception;

public class PersonaDAOException extends Exception {

	/**
	 *
	 */
	private static final long serialVersionUID = 1L;

	public PersonaDAOException() {
		super();

	}

	public PersonaDAOException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}

	public PersonaDAOException(String message, Throwable cause) {
		super(message, cause);

	}

	public PersonaDAOException(String message) {
		super(message);

	}

	public PersonaDAOException(Throwable cause) {
		super(cause);

	}


}
